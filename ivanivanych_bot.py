async def get_available_models() -> Dict[str, List[Tuple[str, float]]]:
    """–°–æ–±–∏—Ä–∞–µ—Ç –∏ —Ç–µ—Å—Ç–∏—Ä—É–µ—Ç –≤—Å–µ –¥–æ—Å—Ç—É–ø–Ω—ã–µ –º–æ–¥–µ–ª–∏."""
